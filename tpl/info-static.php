                        <p style='font-size:18px;color: lime;'>Уполномоченный экономический оператор</p>
                        <p style="text-align: justify;">Это юридическое лицо, отвечающее условиям, указанным в статье 39 Таможенного кодекса Таможенного союза (далее – ТК ТС), которое вправе пользоваться специальными упрощениями, предусмотренными статьей 41 ТК ТС.</p>
                        <p style="text-align: justify;">Статус уполномоченного экономического оператора присваивается юридическому лицу, созданному в соответствии с законодательством государства-члена таможенного союза, на территории которого этому лицу будет присвоен этот статус.</p>
