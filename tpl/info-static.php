                    <p style='font-size:18px;color: lime;'>Таможенный агент (представитель)</p>
                    <p style="text-align: justify;" >Это юридическое лицо-резидент Республики Беларусь, отвечающее требованиям, установленным ТК Республики Беларусь, которому Государственным таможенным комитетом Республики Беларусь выдано специальное разрешение (лицензия) на осуществление деятельности в области таможенного дела в качестве таможенного агента (статья 84 ТмК Республики Беларусь). </p>
                    <p style="text-align: justify;">При совершении таможенных операций таможенный агент (представитель) в соответствии со статьей 86 ТмК Республики Беларусь обладает теми же правами, что и лицо, которое уполномочивает таможенного агента (представителя) представлять свои интересы во взаимоотношениях с таможенными органами.</p></p>
