                        <p style='font-size:18px; color: lime;'>Таможенный склад</p>
                        <p style="text-align: justify;">Это таможенная процедура, при которой иностранные товары хранятся под таможенным контролем на таможенном складе в течение установленного срока без уплаты таможенных пошлин, налогов и без применения мер нетарифного регулирования.При совершении таможенных операций в отношении товаров, помещаемых (помещенных) под таможенную процедуру таможенного склада, необходимо руководствоваться: - главой 33 Таможенного кодекса Таможенного союза;- главой 30 Закона Республики Беларусь «О таможенном регулировании в Республике Беларусь»;- Перечнем товаров, не подлежащих помещению под таможенную процедуру таможенного склада, утвержденным Решением Комиссии Таможенного союза от 20 сентября 2010 г. № 375 «О некоторых вопросах применения таможенных процедур»;- постановлением Государственного таможенного комитета Республики Беларусь от 26 апреля 2012 г. № 11 «О порядке ведения учета и представления отчетности для целей таможенного контроля».</p>
